fun getTirthankaraByNakshatra(nakshatra: String): List<Tirthankara> {
    val normalized = nakshatra.lowercase().replace(Regex("\\s+"), " ")
    return TIRTHANKARAS.filter {
        it.birthNakshatra.lowercase().replace(Regex("\\s+"), " ") == normalized
    }
}